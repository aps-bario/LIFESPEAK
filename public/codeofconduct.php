<DIV class="content">
     <H3>The UK College of Life Coaching Code of Conduct</H3>
      <h4 color="#000066">The Aim</h4>
      <p>
         'To provide an environment in which Coaches can pursue continual professional development
         to attain and maintain the highest possible standards. The College pledges itself
         to the continual implementation of what constitutes best practice in the pursuit
         of excellence within the Coaching profession.'
      </p>
      <h4>The Code</h4>
      <p>
         All staff, tutors, students, graduates, associates and members of The UK College
         of Life Coaching agree to abide by the following:
      </p>
      <h4>Client and Coach Relationship</h4>
      <ol>
         <li>All clients are to be treated with dignity, honour, confidentiality and integrity,
               fully respecting their values, beliefs and goals.
               <br />
            </li>
            <li>All commercial agreements must be honest, fair for all parties concerned and be
               honoured at all times by the Coach.
               <br />
            </li>
            <li>Commercial agreements are to be clearly stated and worded to avoid any confusion.
               All such agreements are to include the expectations of both parties and be fully
               explained at the beginning of the coaching relationship.
               <br />
            </li>
            <li>Coaches shall suggest different coaches or resources when the client's needs would
               be better served elsewhere.
               <br />
            </li>
            <li>Whenever the potential for a conflict of interest arises coaches shall immediately
               discuss the conflict with the client and advise the College. All conflicts of interest
               are to be resolved in whatever way best serves the client.
               <br />
            </li>
            <li>The Code of Conduct will be made freely available to clients upon request.
               <br />
            </li>
            <li>Every Coach shall consider it their responsibility to withdraw from practice if
               life circumstances render them 'unfit' to offer the highest standards of care to
               their client e.g. during periods of ill health.
               <br />
            </li>
            <li>
            In cases where the Coach has a dual qualification and additional professional responsibility
            (e.g. member of the emergency or medical services) it shall become their professional
            responsibility to be aware of any professional conflicts which may arise in terms
            of their duty of care or moral obligation and to seek any clarification needed from
            their relevant professional bodies. 
            </li>
         </ol>
      <h4>Confidentiality</h4>
      <ol>
           <li>Respect the commercial confidentiality of The UK College of Life Coaching by not
               divulging business information to any third parties </li>
            <li>Obtain written permission from any client or potential client before releasing
               their names as referees </li>
            <li>Respect the absolute rights of clients' confidentiality except as expressly permitted
               by the client or potential client in writing or as required by law </li>
            <li>Refrain from offering professional information or advice that you know to be confidential,
               misleading or where the accuracy is beyond your competence to assess </li>
            <li>Respect all copyrights, agreements, work, intellectual property, trademarks and
            comply with all laws covering such areas 
            </li>
         </ol>
      <h4 >Professional Conduct</h4>
      <ol>
            <li>Always conduct themselves to the highest levels of ethics, integrity, accountability
               and responsibility </li>
            <li>Maintain the good reputation of the coaching profession in general and The UK College
               of Life Coaching in particular. </li>
            <li>Comply with both the spirit and the letter of any commercial agreements made with
               clients, potential clients, professional colleagues and The UK College of Life Coaching.
            </li>
            <li>Represent true levels of status, title, competence and experience in order not
               to mislead, misrepresent or defraud </li>
            <li>Clearly state to clients and potential clients, the terms of any commercial agreement
               including the expectations of both parties. </li>
            <li>Make no claims or implications of outcomes that cannot be demonstrated or guaranteed.
            </li>
            <li>Appropriately refer clients to other agencies, resources or coaches when these
               will be more appropriate to the clients' needs. </li>
            <li>Avoid all conflicts of interests and give notice of such potential conflicts The
               UK College of Life Coaching. </li>
            <li>Advise both The UK College of Life Coaching and the client on the risk of conflict
               if a relationship moves beyond a coach/client situation. (For example a personal
               or business relationship.) </li>
            <li>Refrain from offering professional information or advice that you know to be confidential,
               misleading or where the accuracy is beyond your competence to assess </li>
            <li>Respect all copyrights, agreements, work, intellectual property, trademarks and
               comply with all laws covering such areas </li>
            <li>At all times maintain the standards , values and methodologies of the college.
            </li>
            <li>Refrain from coaching minors (people who have not yet had their 18th birthday)
               unless the express, written consent of parent, guardian or teacher as appropriate,
               has been obtained. </li>
            <li>Coaches shall inform The UK College of Life Coaching immediately if a situation
               arises which may create conflict, or generate litigation or bad publicity in relation
               to their Coaching practice. </li>
            <li>Uphold all the laws and bye-laws of the UK and, if working abroad or coaching clients
               based abroad, with the laws of the client's country </li>
            <li>Ensure that all promotional materials, regardless of their format, are legal, decent,
               truthful, honest and comply with the requirements of the UK Advertising Standards
               Authority. 
            </li>
         </ol>
